function addNumber(a:number,b:number) {
    return(a+b)
}

function subNumber(a:number,b:number) {
    return(a-b)
}

function mulNumber(a:number,b:number) {
    return(a*b)
}

var addition_value = addNumber(1,2)
var subtraction_value = subNumber(1,2)
var multiplication_value = mulNumber(1,2)

console.log(addition_value)
console.log(subtraction_value)
console.log(multiplication_value)


